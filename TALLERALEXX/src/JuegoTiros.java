import javax.swing.*;
import java.util.Random;
public class JuegoTiros {

    public static class Jugador {
        private String nombre;
        private int salud;
        private int daño;

        public Jugador() {
            this.nombre = nombre;
            this.salud = 1000;
            this.daño = daño;

        }
        public String getNombre () {
            return nombre;
        }

        public int getDaño() {
            return daño;
        }

        public int getSalud() {
            return salud;
        }


    }


}